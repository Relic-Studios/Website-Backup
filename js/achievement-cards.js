function initAchievementCards() {
    const container = document.getElementById('achievement-cards');
    if (!container) return;

    // Populate cards
    achievements.forEach((achievement, index) => {
        const card = document.createElement('div');
        card.className = 'achievement-card';
        card.setAttribute('data-achievement-index', index);
        card.innerHTML = `
            <h4 class="achievement-card-title">${achievement.title}</h4>
            <p class="achievement-card-description">${achievement.description}</p>
            <div class="achievement-card-meta">${achievement.meta}</div>
        `;

        // Add 3D tilt effect on mouse move
        card.addEventListener('mousemove', (e) => {
            const rect = card.getBoundingClientRect();
            const x = e.clientX - rect.left;
            const y = e.clientY - rect.top;
            const centerX = rect.width / 2;
            const centerY = rect.height / 2;

            // Calculate rotation (-10 to 10 degrees)
            const rotateX = ((y - centerY) / centerY) * -10;
            const rotateY = ((x - centerX) / centerX) * 10;

            // Update CSS custom properties for gradient
            card.style.setProperty('--mouse-x', `${(x / rect.width) * 100}%`);
            card.style.setProperty('--mouse-y', `${(y / rect.height) * 100}%`);

            // Apply 3D transform
            card.style.transform = `perspective(1000px) rotateX(${rotateX}deg) rotateY(${rotateY}deg) translateZ(20px)`;
        });

        // Reset on mouse leave
        card.addEventListener('mouseleave', () => {
            card.style.transform = '';
        });

        // Click to open modal
        card.addEventListener('click', () => {
            openAchievementModal(index);
        });

        container.appendChild(card);
    });

    console.log('Achievement cards initialized with 3D hover effect and modal functionality');
}

// Open Achievement Modal
function openAchievementModal(index) {
    const achievement = achievements[index];
    const modal = document.getElementById('achievement-modal');

    // Populate modal content
    document.getElementById('achievement-modal-title').textContent = achievement.title;
    document.getElementById('achievement-modal-meta').textContent = achievement.meta;

    // Description
    const descriptionContainer = document.getElementById('achievement-modal-description');
    descriptionContainer.innerHTML = achievement.detailedDescription;

    // Highlights
    const highlightsContainer = document.getElementById('achievement-modal-highlights');
    if (achievement.highlights && achievement.highlights.length > 0) {
        highlightsContainer.innerHTML = `
            <h3>Key Achievements</h3>
            <ul>
                ${achievement.highlights.map(highlight => `<li>${highlight}</li>`).join('')}
            </ul>
        `;
    } else {
        highlightsContainer.innerHTML = '';
    }

    // Technologies
    const techContainer = document.getElementById('achievement-modal-tech');
    if (achievement.technologies && achievement.technologies.length > 0) {
        techContainer.innerHTML = `
            <h3>Technologies Used</h3>
            <div class="achievement-tech-tags">
                ${achievement.technologies.map(tech => `<span class="tech-tag">${tech}</span>`).join('')}
            </div>
        `;
    } else {
        techContainer.innerHTML = '';
    }

    // Photo or Instagram Embed
    const photoContainer = document.getElementById('achievement-modal-photo');
    if (achievement.instagramEmbed) {
        photoContainer.innerHTML = `
            <iframe
                src="${achievement.instagramEmbed}"
                width="100%"
                height="580"
                frameborder="0"
                scrolling="no"
                allowtransparency="true"
                style="border-radius: 8px; background: white;">
            </iframe>
        `;
    } else if (achievement.photo) {
        photoContainer.innerHTML = `<img src="${achievement.photo}" alt="${achievement.title}" loading="lazy">`;
    } else {
        photoContainer.innerHTML = '';
    }

    // Video or YouTube Embed
    const videoContainer = document.getElementById('achievement-modal-video');
    if (achievement.youtubeEmbed) {
        videoContainer.innerHTML = `
            <iframe
                width="100%"
                height="315"
                src="${achievement.youtubeEmbed}"
                title="YouTube video player"
                frameborder="0"
                allow="accelerometer; autoplay; clipboard-write; encrypted-media; gyroscope; picture-in-picture; web-share"
                allowfullscreen
                style="border-radius: 8px;">
            </iframe>
        `;
    } else if (achievement.video) {
        videoContainer.innerHTML = `
            <video controls preload="metadata">
                <source src="${achievement.video}" type="video/mp4">
                Your browser does not support the video tag.
            </video>
        `;
    } else {
        videoContainer.innerHTML = '';
    }

    // Show modal with animation
    modal.style.display = 'flex';
    setTimeout(() => {
        modal.classList.add('active');
    }, 10);

    // Prevent body scroll
    document.body.style.overflow = 'hidden';
}

// Close Achievement Modal
function closeAchievementModal() {
    const modal = document.getElementById('achievement-modal');
    modal.classList.remove('active');

    setTimeout(() => {
        modal.style.display = 'none';
        // Re-enable body scroll
        document.body.style.overflow = '';
    }, 400);
}

// Initialize modal close handlers
function initAchievementModalHandlers() {
    const modal = document.getElementById('achievement-modal');
    const closeBtn = document.querySelector('.achievement-modal-close');

    // Close button
    if (closeBtn) {
        closeBtn.addEventListener('click', closeAchievementModal);
    }

    // Click outside modal content
    modal.addEventListener('click', (e) => {
        if (e.target === modal) {
            closeAchievementModal();
        }
    });

    // ESC key
    document.addEventListener('keydown', (e) => {
        if (e.key === 'Escape' && modal.classList.contains('active')) {
            closeAchievementModal();
        }
    });
}

// Smooth scroll enhancement for navigation
function enhanceSmoothScroll() {
    document.querySelectorAll('a[href^="#"]').forEach(anchor => {
        anchor.addEventListener('click', function (e) {
            e.preventDefault();
            const targetId = this.getAttribute('href');
            if (targetId === '#') return;

            const targetElement = document.querySelector(targetId);
            if (targetElement) {
                const navHeight = document.querySelector('#main-nav')?.offsetHeight || 80;
                const targetPosition = targetElement.offsetTop - navHeight - 20;

                window.scrollTo({
                    top: targetPosition,
                    behavior: 'smooth'
                });

                // Update active nav link
                document.querySelectorAll('.nav-link').forEach(link => {
                    link.classList.remove('active');
                });
                this.classList.add('active');
            }
        });
    });

    console.log('Smooth scroll enhanced');
}

// Initialize on DOM load
if (document.readyState === 'loading') {
    document.addEventListener('DOMContentLoaded', () => {
        initAchievementCards();
        initAchievementModalHandlers();
        enhanceSmoothScroll();
    });
} else {
    initAchievementCards();
    initAchievementModalHandlers();
    enhanceSmoothScroll();
}
